package com.zyj.plugin.common.data.bean;

public class CityInfoBean {

    /**
     * id : 110100
     * cname : 北京市
     * pid : 110000
     */

    private int id;
    private String cname;
    private int pid;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getCname() {
        return cname;
    }

    public void setCname(String cname) {
        this.cname = cname;
    }

    public int getPid() {
        return pid;
    }

    public void setPid(int pid) {
        this.pid = pid;
    }
}
